            <div class="second-content">
              <div class="title">Apa itu stunting?</div>
              <img src="<?= base_url('assets/img/Capture_2.png'); ?>" width="100%" alt="">
              <p class="subtitle">Mengutip dari Buletin Stunting yang dikeluarkan oleh Kementerian Kesehatan RI, stunting adalah kondisi yang ditandai ketika panjang atau tinggi badan anak kurang jika dibandingkan dengan umurnya.</p>
              <p class="subtitle">Mudahnya, stunting adalah kondisi di mana anak mengalami gangguan pertumbuhan sehingga menyebabkan tubuhnya lebih pendek ketimbang teman-teman seusianya dan memiliki penyebab utama kekurangan nutrisi.</p>
              <p class="subtitle">Banyak yang tidak tahu kalau anak pendek adalah tanda dari adanya masalah gizi kronis pada pertumbuhan tubuh si kecil. Hanya saja, perlu diingat bahwa anak pendek belum tentu stunting, sedangkan anak stunting pasti terlihat pendek.</p>
              <p class="subtitle">Anak masuk ke dalam kategori stunting ketika panjang atau tinggi badannya menunjukkan angka di bawah -2 standar deviasi (SD). Terlebih lagi, jika kondisi ini dialami anak yang masih di bawah usia 2 tahun dan harus ditangani dengan segera dan tepat.</p>
              <p class="subtitle">Penilaian status gizi dengan standar deviasi tersebut biasanya menggunakan grafik pertumbuhan anak (GPA) dari WHO.</p>
              <p class="subtitle">Tubuh pendek pada anak yang berada di bawah standar normal merupakan akibat dari kondisi kurang gizi yang telah berlangsung dalam waktu lama.</p>
              <p class="subtitle">Hal tersebut yang kemudian membuat pertumbuhan tinggi badan anak terhambat sehingga mengakibatkan dirinya tergolong stunting.</p>
              <p class="subtitle">Namun, anak dengan tubuh pendek belum tentu serta merta mengalami stunting. Kondisi ini hanya terjadi ketika asupan nutrisi harian anak kurang sehingga memengaruhi perkembangan tinggi badannya.</p>
