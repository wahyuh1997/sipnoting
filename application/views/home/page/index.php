              <img src="<?= base_url('assets/img/Capture_3.png'); ?>" width="100%" alt="">
              <p class="subtitle">Masalah kesehatan ini merupakan akibat dari berbagai faktor yang terjadi pada masa lalu. Berbagai faktor tersebut antara lain asupan gizi yang buruk, berkali-kali terserang penyakit infeksi, bayi lahir prematur, serta berat badan lahir rendah (BBLR).</p>
              <p class="subtitle">Kondisi tidak tercukupinya asupan gizi anak ini biasanya tidak hanya terjadi setelah ia lahir saja, melainkan bisa dimulai sejak ia masih di dalam kandungan.</p>
              <p class="subtitle">Di bawah ini dua poin utama yang menjadi faktor penyebab stunting pada anak.</p>
              <ol class="subtitle">
                <li class="subtitle">Kurang asupan gizi selama hamil</li>
                <p class="subtitle">WHO atau badan kesehatan dunia menyatakan bahwa sekitar 20% kejadian stunting sudah terjadi saat bayi masih berada di dalam kandungan.
                  Hal ini disebabkan oleh asupan ibu selama hamil yang kurang bergizi dan berkualitas sehingga nutrisi yang diterima janin cenderung sedikit.
                  Akhirnya, pertumbuhan di dalam kandungan mulai terhambat dan terus berlanjut setelah kelahiran. Oleh karena itu, penting untuk mencukupi berbagai nutrisi penting selama hamil.</p>
                <li class="subtitle">Kebutuhan gizi anak tidak tercukupi</li>
                <p class="subtitle">Selain itu, kondisi ini juga bisa terjadi akibat makanan balita saat masih di bawah usia 2 tahun yang tidak tercukupi, seperti posisi menyusui yang kurang tepat, tidak diberikan ASI eksklusif, hingga MPASI (makanan pendamping ASI) yang kurang berkualitas.</p>
              </ol>
              <p class="subtitle">Banyak teori yang menyatakan bahwa kurangnya asupan makanan juga bisa menjadi salah satu faktor utama penyebab stunting. Khususnya asupan makanan yang mengandung protein serta mineral zinc (seng) dan zat besi ketika anak masih berusia balita.</p>
              <p class="subtitle">Melansir buku Gizi Anak dan Remaja, kejadian ini umumnya sudah mulai berkembang saat anak berusia 3 bulan. Proses perkembangan tersebut lambat laun mulai melambat ketika anak berusia 3 tahun.</p>
